package com.nashtech.demoqa.steps.modals;

public class StudentData {
    public String firstName;
    public String lastName;
    public String email;
    public String gender;
    public String mobile;
    public String dateOfBird;
    public String subject;
    public String hobby;
    public String picture;
    public String currentAddress;
    public String state;
    public String city;

    public StudentData(String firstName, String lastName, String email, String gender, String mobile, String dateOfBird, String subject, String hobby, String picture, String currentAddress, String state, String city) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.gender = gender;
        this.mobile = mobile;
        this.dateOfBird = dateOfBird;
        this.subject = subject;
        this.hobby = hobby;
        this.picture = picture;
        this.currentAddress = currentAddress;
        this.state = state;
        this.city = city;
    }

}

